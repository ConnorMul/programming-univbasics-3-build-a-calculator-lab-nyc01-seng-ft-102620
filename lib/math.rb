def addition(num1, num2)
  sum = num2 + num1
  sum
end

def subtraction(num1, num2)
  minus = num2 - num1
  minus
end

def division(num1, num2)
  div = num2 / num1
  div
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  mod = num2 % num1
  mod
end

def square_root(num)
  Math.sqrt(num)
end